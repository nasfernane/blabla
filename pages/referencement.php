        <h2> Une transition éco-responsable </h2><br/>
        <p>La mesure de l’empreinte carbone générée par toutes les productions physiques ou numériques sur notre planète est devenue un sujet de société incontournable.</p><br/>

        <p>L’urgence environnementale, et la prise de conscience que l’ampleur de la pollution numérique  joue actuellement un rôle critique dans le réchauffement climatique nous ont poussés à dresser un bilan constructif nécessaire.</p><br/>
        
        <p>C’est notre responsabilité, en tant que leaders du référencement de sites web, de mettre en oeuvre la
        conduite du changement  sur les bonnes pratiques de
        l’éco-conception.</p><br/>
        
        <p>Pour ce faire, nous appelons les développeurs débutants ou plus expérimentés à intégrer ces bonnes pratiques au quotidien dans leurs missions de codage des sites de demain.</p><br/>
        
        <p>L’objectif de cette démarche est de commencer à avoir un impact fort, visant à diminuer la production de CO2 par l’ensemble de la filière web et ce à l’horizon de l’été 2021.
        L’eco-conception sera valorisée directement et deviendra un levier majeur en rejoignant la liste des critères primordiaux pour l’obtention d’un bon référencement.</p><br/>
