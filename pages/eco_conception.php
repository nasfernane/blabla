            <h1>Les bonnes pratiques d’un code eco-responsable</h1><br/>
            <p><strong>Images en Full HD</strong> :
            A éviter car elles forcent les internautes à les télécharger à chaque passage sur votre site. Il sera question de plusieurs mégaoctets en comparaison d’une petite image qui ne ferait que quelques Kilooctets.</p><br/>

            <p><strong>Les polices de caractère</strong> :
            Réduire leur nombre sur un même site web. Cela sera moins polluant.</p><br/>

            <p><strong>Le contenu</strong> :
            Il doit être le plus léger possible afin de réduire la dépense énergétique.</p><br/>

            <p><strong>Un bon chargement</strong> :
            Une vitesse optimisée du chargement des pages webs réduit leur consommation.</p><br/>

            <p><strong>Fonctions inutiles</strong> :
            45% des fonctionnalités ne sont jamais utilisées sur les sites. Nous vous conseillons donc d’alléger au maximum les animations (survol du texte par exemple) ou de trouver des alternatives moins consommatrices.</p>
