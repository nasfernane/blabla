            <h1>Repenser internet sur un modèle eco-responsable</h1><br/>

            <p>Contrairement aux idées reçues, un site web n’est pas forcément très écologique. Bonne nouvelle, certaines améliorations permettent de rendre un site eco-responsable.
            Un site eco-responsable est un site qui s'efforce de respecter la nature et l'environnement au maximum.</p><br/>
            <p>En effet, si le numérique remplace dans de nombreux domaines le papier, il n’en est pas moins polluant. Or, la protection de l’environnement est au cœur de toutes les préoccupations ces dernières années.
            Ainsi, les développeurs ont réfléchie conseils à mettre en pratique pour déployer un site éco-friendly.</p>
